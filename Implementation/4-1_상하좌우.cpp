#include<iostream>
#include<string>
using namespace std;

int x = 1, y = 1; // 초기 위치

// L, R, U, D에 따른 이동 방향
int dx[4] = {0, 0, -1, 1};
int dy[4] = {-1, 1, 0, 0};
char moveTypes[4] = {'L', 'R', 'U', 'D'};

int main() {
	int n;
	cin >> n; // 정사각행렬의 크기

	// cin으로 입력 받으면 엔터가 버퍼에 남아있기 때문에,
	// 다음 입력을 받기 위해서는 반드시 버퍼를 비워줘야 한다.
	cin.ignore(); 

	// 몇개의 문자를 입력할지 모르기 때문에 문자열 한 줄 전체를 입력 받는다.
	string plans;
	getline(cin, plans);

	// 이동 계획을 하나씩 확인
    for (int i = 0; i < plans.size(); i++) {
        char plan = plans[i];

        // 이동 후 좌표 구하기 
        int nx = -1, ny = -1;
        for (int j = 0; j < 4; j++) {
            if (plan == moveTypes[j]) {
                nx = x + dx[j];
                ny = y + dy[j];
            }
        }

        // 공간을 벗어나는 경우 무시 
        if (nx < 1 || ny < 1 || nx > n || ny > n) 
			continue;

        // 공간을 벗어나지 않을 때만 이동 수행 
        x = nx;
        y = ny;
    }

    cout << x << ' ' << y << '\n';

    return 0;
}